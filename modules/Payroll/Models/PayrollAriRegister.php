class PayrollAriRegister extends Model implements Auditable
{
    use SoftDeletes;
    use AuditableTrait;
    use ModelsTrait;

    /**
     * Lista de atributos para la gestiÃ³n de fechas
     *
     * @var array $dates
     */
    protected $dates = ['deleted_at'];

    /**
     * Lista de atributos que pueden ser asignados masivamente
     *
     * @var array $fillable
     */
    protected $fillable = ['payroll_staff_id', 'from_date', 'to_date', 'percetage'];

    /**
     * Trabajador asociado al registro ARI
     *
     * @return \Illuminate\Database\Eloquent\Relations\BelongsTo
     */
